            <h1> Thematical Appreception Test</h1>
            <p>Thematic Apperception Test is the second objective test employed by the SSBs to test the personality of the candidate. It is also a test of the candidate’s imagination. If an outline or hazy and vague picture is shown to a candidate, he will interpret it according to his imagination. These pictures, therefore, stimulate the thought processes of the candidate and he is able to weave around it a story.
            </p>
            <p> It is an attempt to let the candidate have flights of imagination. It is a method of exploring matter from a person’s subconscious mind by stimulating his fantasy and interpreting it. The candidate is encouraged to come out with ideas and inner hidden or latent material which will not be available to us normally.
            </p>
            <h2><u>Why  Thematic Apperception Test?</u></h2>
            <p>This pictures originally employed for this purpose were very vague. There was a great play of projection or inner subconsciousness in the interpretation of these T.A.T pictures  Pictures with greater  details and needing less play of projection and imagination were introduced by two psychologist, Murray and Margon, who used these in 1903 and the same are generally in use in India.
            </p>
            <p>In TAT, the picture used by the SSBs are on the model of Murray’s pictures, but they are almost clear and not hazy. A set of 12 pictures is generally used including one blank slide. A slide of each picture is shown on a screen with the help of a magic lantern for half a minute and the candidates are required to write a story based on the picture in a period of four minutes in the particular space provided on the answer sheet.
            </p>
            <p>These test is based on the theory that in the construction or stories around ambiguous pictures, the candidate organize material from his own personal experiences, partly as a result of the stimulus that he receives i.e. what he finds in the picture and partly his associations with such stimuli recalled from his past experiences.
            </p>
